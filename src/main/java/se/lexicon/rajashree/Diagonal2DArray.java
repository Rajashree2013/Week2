public class Diagonal2DArray {

    public static void main(String[] args) {


        int num = 1;
        int[][] a = new int[3][3];
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {


                a[i][j] = num+1;

                System.out.print(a[i][j]);

            }



        }}}